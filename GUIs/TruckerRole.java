package GUIs;

public class TruckerRole
{

    private int IDNumber;
    private String password;
    private String name;


    public TruckerRole()
    {
        IDNumber = -1;
        password = "NOT INPUT";
        name = "DEFAULT";
    }

    public TruckerRole(int IDNumber, String password, String name)
    {
        this.IDNumber = IDNumber;
        this.password = password;
        this.name = name;
    }





}
